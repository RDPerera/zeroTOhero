public class SwitchCase
{
    public static void main(String [] args)
    {
        char a='A';
        switch(a)
        {
            case 'A' : System.out.println("A");break;
            case 'B' : System.out.println("B");break;
            case 'C' : System.out.println("C");break;
            default : System.out.println("Not A,B,C");
        }
    }
}